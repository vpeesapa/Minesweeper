public class Choice
{
	private int playerChoiceX;
	private int playerChoiceY;
	Choice()
	{
		playerChoiceX=0;
		playerChoiceY=0;
	}
	public void setChoiceX(int choiceX)
	{
		playerChoiceX=choiceX;
	}
	public int getChoiceX()
	{
		return playerChoiceX;
	}
	public void setChoiceY(int choiceY)
	{
		playerChoiceY=choiceY;
	}
	public int getChoiceY()
	{
		return playerChoiceY;
	}
}